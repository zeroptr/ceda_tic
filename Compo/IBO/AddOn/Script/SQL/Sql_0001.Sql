CREATE DOMAIN NAME AS VARCHAR(50);
CREATE DOMAIN SHORTNAME AS VARCHAR(25);
CREATE DOMAIN GENDER AS CHAR(1);
CREATE DOMAIN BOOLEAN AS CHAR(1)
              CHECK ( VALUE IN (' ', 'Y', 'N') OR 
                      VALUE IS NULL);
CREATE DOMAIN OBLAST_ID AS CHAR(2) NOT NULL;
CREATE DOMAIN ROVD_ID AS CHAR(3) NOT NULL;
CREATE DOMAIN D_ID AS INTEGER NOT NULL;
CREATE DOMAIN CLERK_SIGN AS VARCHAR(15);
CREATE DOMAIN FILE_PATH AS VARCHAR(240);
CREATE DOMAIN PICTURE AS BLOB SUB_TYPE 0 SEGMENT SIZE 4096;
CREATE DOMAIN NOTE AS BLOB SUB_TYPE TEXT SEGMENT SIZE 80;
CREATE DOMAIN PSERIA  AS VARCHAR(2)  CHECK ( VALUE LIKE ('__') OR
                                                           VALUE IS NULL );
CREATE DOMAIN PNUM    AS VARCHAR(6)  CHECK ( VALUE LIKE ('______') OR
                                                           VALUE IS NULL );
